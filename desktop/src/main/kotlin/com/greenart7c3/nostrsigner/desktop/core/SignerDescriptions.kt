        "nip" -> nipDescription(kind, language).ifEmpty { Strings.format("event_kind", kind.toString(), language = language) }
        else -> type
    }

    /** The event-kind label alone (e.g. "Short text note"), localized. */
    fun signEventDescription(kind: Int?, language: String = Strings.currentLanguage.value): String {
        val key = signEventKindKey(kind)
        if (key.isNotEmpty()) return Strings.get(key, language)
        val nip = nipDescription(kind, language)
        return nip.ifEmpty { Strings.format("event_kind", kind.toString(), language = language) }
    }

    private fun nipDescription(kind: Int?, language: String): String {
        val key = nipKey(kind)
        return if (key.isEmpty()) "" else Strings.get(key, language)
    }

